// for of, iterates through the list of elements
// like array and returns the elements one by one.
// for of outputs the values not the index
let arr = [2, 3, 4.5, 5, 6];

for (let value of arr) {
	console.log(value);
}

let stringy = 'javascript';

for (let char of stringy) {
	console.log(char);
}